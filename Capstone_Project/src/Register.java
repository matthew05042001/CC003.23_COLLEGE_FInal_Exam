     public static void filecheck() throws FileNotFoundException, IOException, ParseException {
        File file = new File(filepath);

        // If the file does not exist or is empty, initialize with an empty JSON structure
        if (!file.exists() || file.length() == 0) {
            record = new JSONObject();
            userlist = new JSONArray();
            record.put("users", userlist);
            save(); // Save an empty structure to prevent errors
            return;
        }

        try (FileReader reader = new FileReader(file)) {
            JSONParser jsonParser = new JSONParser();
            record = (JSONObject) jsonParser.parse(reader);
            userlist = (JSONArray) record.get("users");

            // Ensure userlist is initialized
            if (userlist == null) {
                userlist = new JSONArray();
            }
        } catch (ParseException | IOException e) {
            System.out.println("Error loading JSON file: " + e.getMessage());
        }
    }

    public static void save() throws IOException {
        try (FileWriter file = new FileWriter(filepath)) {
            file.write(record.toJSONString());
        }
    }
