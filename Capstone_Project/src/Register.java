    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            filecheck(); // Load existing JSON data

            String adname = jTextField1.getText().trim();
            String adpass = jTextField2.getText().trim();  // Password field
            String adpassConfirm = jTextField4.getText().trim();  // Confirm password field (now jTextField4)
            String adbirth = jTextField3.getText().trim();

            // Check for blank fields
            if (adname.isEmpty() || adpass.isEmpty() || adpassConfirm.isEmpty() || adbirth.isEmpty()) {
                JOptionPane.showMessageDialog(null, "All fields are required!", "Error", JOptionPane.ERROR_MESSAGE);
                return; // Stop execution
            }

            // Check if passwords match
            if (!adpass.equals(adpassConfirm)) {
                JOptionPane.showMessageDialog(null, "Passwords do not match!", "Error", JOptionPane.ERROR_MESSAGE);
                return; // Stop execution
            }

            // Ensure record and userlist are properly initialized
            if (record == null) {
                record = new JSONObject();
            }
            if (userlist == null) {
                userlist = new JSONArray();
            }

            // Check for existing username
            for (Object obj : userlist) {
                JSONObject existingUser = (JSONObject) obj;
                if (adname.equalsIgnoreCase((String) existingUser.get("username"))) {
                    JOptionPane.showMessageDialog(null, "Username already exists!", "Error", JOptionPane.ERROR_MESSAGE);
                    return; // Stop execution
                }
            }

            // Create new user object
            JSONObject user = new JSONObject();
            user.put("username", adname);
            user.put("password", adpass);
            user.put("Birthday", adbirth);
            user.put("type", "member"); // Always "member"

            // Add the new user to the existing list
            userlist.add(user);
            record.put("users", userlist); // Ensure users are updated in the record

            save(); // Save the updated JSON file
            JOptionPane.showMessageDialog(null, "Successfully Registered!", "Success", JOptionPane.INFORMATION_MESSAGE);

        } catch (IOException | ParseException ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

